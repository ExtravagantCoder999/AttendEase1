class ClassInfo {
  final String section;
  final String subject;
  final String room;
  final String time;
  final int present;
  final int absent;
  final int excused;

  ClassInfo({
    required this.section,
    required this.subject,
    required this.room,
    required this.time,
    required this.present,
    required this.absent,
    required this.excused,
  });
}
